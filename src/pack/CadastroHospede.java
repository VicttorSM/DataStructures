    private void cadastrar() {
        String nome = txt_nome.getText();
        String sobrenome = txt_sobrenome.getText();
        String idade = txt_idade.getText();
        String sexo = combo_sexo.getSelectedItem().toString();
        String cpf = txt_cpf.getText();
        String cidade = txt_cidade.getText();
        String email = txt_email.getText();
        String telefone = txt_tel.getText();
        

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo NOME Ã© obrigatÃ³rio!","AVISO", 2);
        }

        else if (sobrenome.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo SOBRENOME Ã© obrigatÃ³rio!","AVISO", 2);
        }

        else if (idade.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo IDADE Ã© obrigatÃ³rio!","AVISO", 2);
        }
        
        // Verifica se idade eh um numero
        else if (!idade.matches("-?\\d+")) {
            JOptionPane.showMessageDialog(null, "Campo IDADE precisa ser um nÃºmero inteiro!","AVISO", 2);
        }

        else if (cpf.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo CPF Ã© obrigatÃ³rio!","AVISO", 2);
        }
        
        // Verifica se o cpf jÃ¡ foi cadastrado
        else if (hotel.getHospedePorCPF(cpf) != null) {
            JOptionPane.showMessageDialog(null, "O CPF '" + cpf + "' jÃ¡ foi cadastrado","AVISO", 2);
        }
        
        else if(cpf.trim().length() < 14) {
                JOptionPane.showMessageDialog(null, "Por favor, insira um CPF vÃ¡lido", "AVISO", 2);
                txt_cpf.requestFocusInWindow();
                
        }

        else if (telefone.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo TELEFONE Ã© obrigatÃ³rio!","AVISO", 2);
        }
        
        else{
            Hospede hospede = 
                    new Hospede(
                            nome,
                            sobrenome,
                            Integer.valueOf(idade),
                            cpf,
                            sexo,
                            cidade,
                            email,
                            telefone);
            hotel.cadastrarHospede(hospede);
            JOptionPane.showMessageDialog(null, "HÃ³spede cadastrado com sucesso!","CADASTRADO", 1);

            txt_nome.setText("");
            txt_sobrenome.setText("");
            txt_idade.setText("");
            txt_cpf.setText("");
            txt_cidade.setText("");
            txt_email.setText("");
            txt_tel.setText("");
            numDeCadastros--;
            if (numDeCadastros == 0) {
                new CadastroReserva(hotel).show();
                dispose();
            }
        }
    }
    
